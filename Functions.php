<!-- functions.php -->
<?php
function add($a, $b) {
    return $a + $b;
}

function subtract($a, $b) {
    return $a - $b;
}

function multiply($a, $b) {
    return $a * $b;
}

function divide($a, $b) {
    return $a / $b;
}

function exponent($a, $b) {
    return pow($a, $b);
}

function percentage($a, $b) {
    return ($a / 100) * $b;
}

function sqrt_calc($a) {
    return sqrt($a);
}

function log_calc($a) {
    return log($a);
}
?>
